//! Schema-driven CLI generation.
//!
//! Builds a clap `Command` tree from a tool's MCP schema (the output of
//! [`crate::schema::generate_mcp_schema`]). The generator reads the `op` enum's
//! `verb noun` values and `x-operation-schemas` to produce a `noun → verb → args`
//! command tree where each verb's arguments are scoped to exactly the parameters
//! that operation accepts — not the global union of every operation's parameters.
//!
//! This engine is tool-agnostic: it consumes whatever full schema it is handed,
//! so every CLI built on the `Operation` trait (kanban-cli, sah, code-context-cli,
//! shelltool-cli) can share it.
//!
//! The two public entry points are:
//! - [`build_commands_from_schema`] — schema → clap command tree.
//! - [`extract_noun_verb_arguments`] — clap matches → `{ "op": "verb noun", ...args }`.

use clap::{Arg, ArgAction, Command};
use once_cell::sync::Lazy;
use serde_json::Value;
use std::collections::{HashMap, HashSet};
use std::sync::Mutex;

/// The synthetic field name carrying the `"verb noun"` op string.
///
/// This key is generated by the engine (not a real schema property) and is read
/// and written at several sites; naming it once keeps those sites in lockstep.
const OP_FIELD: &str = "op";

/// JSON Schema `type` token for a boolean property.
///
/// The JSON Schema type names form a small closed vocabulary that the engine
/// matches on at several sites (type probing, arg-type resolution, value
/// extraction); naming each token once keeps those sites in lockstep and the
/// vocabulary in a single place.
const TYPE_BOOLEAN: &str = "boolean";

/// JSON Schema `type` token for the null type (used in nullable unions).
const TYPE_NULL: &str = "null";

/// JSON Schema `type` token for an array property.
const TYPE_ARRAY: &str = "array";

/// JSON Schema `type` token for an integer property.
const TYPE_INTEGER: &str = "integer";

/// JSON Schema `type` token for a (floating-point) number property.
const TYPE_NUMBER: &str = "number";

// ---------------------------------------------------------------------------
// String interning (clap requires 'static lifetimes)
// ---------------------------------------------------------------------------

/// Cache of interned strings handed out as `&'static str` to clap.
static STRING_CACHE: Lazy<Mutex<HashSet<&'static str>>> = Lazy::new(|| Mutex::new(HashSet::new()));

/// Intern a string, returning a `&'static str` clap can hold.
///
/// clap's builder API requires `'static` names/help text. Schema-derived
/// strings are owned at runtime, so we leak them once and reuse the leaked
/// reference for any equal string thereafter (bounded by the schema size).
fn intern(s: String) -> &'static str {
    let mut cache = STRING_CACHE.lock().unwrap();
    if let Some(&cached) = cache.get(s.as_str()) {
        return cached;
    }
    let leaked: &'static str = Box::leak(s.into_boxed_str());
    cache.insert(leaked);
    leaked
}

// ---------------------------------------------------------------------------
// Schema helpers
// ---------------------------------------------------------------------------

/// Return `true` if a property schema declares `type_name`, handling both
/// a plain string `type` and a `type` array (e.g. `["boolean", "null"]`).
fn schema_has_type(schema: &Value, type_name: &str) -> bool {
    match schema.get("type") {
        Some(Value::String(t)) => t.as_str() == type_name,
        Some(Value::Array(types)) => types.iter().any(|t| t.as_str() == Some(type_name)),
        _ => false,
    }
}

/// Return the primary (non-null) JSON Schema type of a property, if any.
///
/// For a `type` array such as `["string", "null"]` the first non-`null`
/// entry wins; for a plain string `type` that string is returned.
fn primary_type(schema: &Value) -> Option<&str> {
    match schema.get("type") {
        Some(Value::String(t)) => Some(t.as_str()),
        Some(Value::Array(types)) => types
            .iter()
            .find_map(|t| t.as_str().filter(|s| *s != TYPE_NULL)),
        _ => None,
    }
}


/// Precomputed clap argument metadata derived from one JSON schema property.
#[derive(Debug, Clone)]
struct ArgMeta {
    /// The parameter name (becomes the clap arg id and `--long` flag).
    name: String,
    /// Help text from the property's `description`, if any.
    help: Option<String>,
    /// Whether the parameter is required for this operation.
    is_required: bool,
    /// The clap value type to configure for this argument.
    arg_type: ArgMetaType,
    /// Default value from the property's `default`, if any.
    default_value: Option<String>,
    /// Allowed values from the property's `enum`, if any.
    possible_values: Option<Vec<String>>,
}

/// The clap-relevant value type of an argument, derived from the JSON Schema type.
#[derive(Debug, Clone)]
enum ArgMetaType {
    /// A plain string value.
    String,
    /// A 64-bit signed integer.
    Integer,
    /// A 64-bit float.
    Float,
    /// A flag (`--flag` with no value, sets `true`).
    Boolean,
    /// A nullable boolean accepting an explicit `true`/`false` value.
    NullableBoolean,
    /// A repeatable string value collected into an array.
    Array,
}

/// Parse a schema's `required` list into a set of required parameter names.
///
/// Returns an empty set when there is no `required` array.
fn required_param_names(schema: &Value) -> HashSet<String> {
    schema
        .get("required")
        .and_then(|r| r.as_array())
        .map(|arr| {
            arr.iter()
                .filter_map(|v| v.as_str())
                .map(|s| s.to_string())
                .collect()
        })
        .unwrap_or_default()
}

/// Resolve the clap value type for a single JSON schema property.
///
/// A property typed `["boolean", "null"]` becomes [`ArgMetaType::NullableBoolean`];
/// otherwise the primary (non-null) JSON Schema type selects the variant, with
/// any unknown or absent type falling back to [`ArgMetaType::String`].
fn resolve_arg_type(prop_schema: &Value) -> ArgMetaType {
    if schema_has_type(prop_schema, TYPE_BOOLEAN) && schema_has_type(prop_schema, TYPE_NULL) {
        return ArgMetaType::NullableBoolean;
    }
    match primary_type(prop_schema) {
        Some(TYPE_BOOLEAN) => ArgMetaType::Boolean,
        Some(TYPE_INTEGER) => ArgMetaType::Integer,
        Some(TYPE_NUMBER) => ArgMetaType::Float,
        Some(TYPE_ARRAY) => ArgMetaType::Array,
        _ => ArgMetaType::String,
    }
}

/// Convert a schema (an op schema or the global schema) into ordered [`ArgMeta`].
///
/// Reads `properties` and the `required` list. Array parameters are never marked
/// required (they default to empty in CLI context) and the synthetic `op`
/// parameter is never required (it is set by the noun-verb path). The clap value
/// type for each property is resolved by [`resolve_arg_type`].
fn precompute_args(schema: &Value) -> Vec<ArgMeta> {
    let properties = match schema.get("properties").and_then(|p| p.as_object()) {
        Some(props) => props,
        None => return Vec::new(),
    };

    let required = required_param_names(schema);

    properties
        .iter()
        .map(|(name, prop_schema)| {
            // In CLI context, arrays default to empty so never require them.
            // Also skip requiring the op field — it's set by the noun-verb path.
            let is_required = required.contains(name)
                && !matches!(primary_type(prop_schema), Some(TYPE_ARRAY))
                && name != OP_FIELD;

            ArgMeta {
                name: name.clone(),
                help: prop_schema
                    .get("description")
                    .and_then(|v| v.as_str())
                    .map(String::from),
                is_required,
                arg_type: resolve_arg_type(prop_schema),
                default_value: prop_schema
                    .get("default")
                    .and_then(|v| v.as_str())
                    .map(String::from),
                possible_values: prop_schema
                    .get("enum")
                    .and_then(|v| v.as_array())
                    .map(|arr| {
                        arr.iter()
                            .filter_map(|v| v.as_str().map(String::from))
                            .collect()
                    }),
            }
        })
        .collect()
}

/// Build a clap [`Arg`] from precomputed [`ArgMeta`].
///
/// Maps the arg type to the right clap value parser / action, wires up
/// required-ness, help, default value, and `enum`-derived possible values.
fn build_clap_arg(meta: &ArgMeta) -> Arg {
    let name_static = intern(meta.name.clone());
    let mut arg = Arg::new(name_static).long(name_static);

    if meta.is_required {
        arg = arg.required(true);
    }
    if let Some(ref help) = meta.help {
        arg = arg.help(intern(help.clone()));
    }
    if let Some(ref default) = meta.default_value {
        arg = arg.default_value(intern(default.clone()));
    }
    if let Some(ref values) = meta.possible_values {
        let strs: Vec<&'static str> = values.iter().map(|s| intern(s.clone())).collect();
        arg = arg.value_parser(clap::builder::PossibleValuesParser::new(strs));
    }

    arg = match meta.arg_type {
        ArgMetaType::Boolean => arg.action(ArgAction::SetTrue),
        ArgMetaType::NullableBoolean => arg
            .value_parser(clap::builder::PossibleValuesParser::new(["true", "false"]))
            .value_name("BOOL"),
        ArgMetaType::Integer => {
            let mut a = arg.value_parser(clap::value_parser!(i64));
            if !meta.is_required {
                a = a.value_name("NUMBER");
            }
            a
        }
        ArgMetaType::Float => {
            let mut a = arg.value_parser(clap::value_parser!(f64));
            if !meta.is_required {
                a = a.value_name("NUMBER");
            }
            a
        }
        ArgMetaType::Array => {
            let mut a = arg.action(ArgAction::Append);
            if !meta.is_required {
                a = a.value_name("VALUE");
            }
            a
        }
        ArgMetaType::String => {
            if !meta.is_required {
                arg.value_name("TEXT")
            } else {
                arg
            }
        }
    };

    arg
}


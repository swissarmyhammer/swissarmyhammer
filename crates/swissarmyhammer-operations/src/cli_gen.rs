/// Build the `noun → verb` command tree from a tool's MCP schema.
///
/// Uses `x-operation-schemas` to scope each verb's args to only the parameters
/// that operation actually accepts (so required flags are correct per op, not
/// the global union). Falls back to the global `properties` block when a schema
/// has no `x-operation-schemas`.
///
/// # Arguments
///
/// * `schema` - A full MCP schema as produced by [`crate::schema::generate_mcp_schema`].
///
/// # Returns
///
/// A list of top-level clap [`Command`]s, one per noun, each with its verbs as
/// subcommands. The list is sorted by noun name. Returns an empty vec when the
/// schema has no `properties.op.enum`.
pub fn build_commands_from_schema(schema: &Value) -> Vec<Command> {
    // Per-op arg maps (keyed by op string, e.g. "init board"); empty when the
    // schema carries no x-operation-schemas.
    let op_args = build_op_args(schema);

    // Fall back to the global schema args only when no per-op schemas exist.
    let fallback_args = if op_args.is_empty() {
        precompute_args(schema)
    } else {
        Vec::new()
    };

    // Parse the op enum into the "verb noun" values; absent -> no commands.
    let op_values = match schema
        .get("properties")
        .and_then(|p| p.get(OP_FIELD))
        .and_then(|op| op.get("enum"))
        .and_then(|e| e.as_array())
    {
        Some(arr) => arr,
        None => return Vec::new(),
    };

    let mut commands: Vec<Command> = group_verbs_by_noun(op_values)
        .into_iter()
        .map(|(noun, verbs)| build_noun_command(&noun, &verbs, &op_args, &fallback_args))
        .collect();
    commands.sort_by(|a, b| a.get_name().cmp(b.get_name()));
    commands
}

/// Build the per-operation arg map from a schema's `x-operation-schemas`.
///
/// Each entry is keyed by the op `title` (e.g. `"init board"`) and carries that
/// operation's `description` plus its precomputed [`ArgMeta`] (so required flags
/// are scoped to exactly that op, not the global union). Returns an empty map
/// when the schema has no `x-operation-schemas` array.
fn build_op_args(schema: &Value) -> HashMap<String, (String, Vec<ArgMeta>)> {
    let mut op_args: HashMap<String, (String, Vec<ArgMeta>)> = HashMap::new();
    let Some(op_schemas) = schema.get("x-operation-schemas").and_then(|v| v.as_array()) else {
        return op_args;
    };
    for op_schema in op_schemas {
        let Some(title) = op_schema.get("title").and_then(|t| t.as_str()) else {
            continue;
        };
        let desc = op_schema
            .get("description")
            .and_then(|d| d.as_str())
            .unwrap_or("")
            .to_string();
        op_args.insert(title.to_string(), (desc, precompute_args(op_schema)));
    }
    op_args
}

/// Group `"verb noun"` op enum values by noun.
///
/// Each op string is split on the first space into `verb` and `noun`; values that
/// are not a `"verb noun"` pair are skipped. Returns a map of noun to its
/// `(verb, op_string)` pairs, where `op_string` is the original value used to look
/// up per-op args.
fn group_verbs_by_noun(op_values: &[Value]) -> HashMap<String, Vec<(String, String)>> {
    let mut noun_groups: HashMap<String, Vec<(String, String)>> = HashMap::new();
    for op_val in op_values {
        let Some(op_str) = op_val.as_str() else {
            continue;
        };
        let parts: Vec<&str> = op_str.splitn(2, ' ').collect();
        if parts.len() == 2 {
            noun_groups
                .entry(parts[1].to_string())
                .or_default()
                .push((parts[0].to_string(), op_str.to_string()));
        }
    }
    noun_groups
}

/// Build a single noun command with its verbs as sorted subcommands.
///
/// # Arguments
///
/// * `noun` - The noun name (e.g. `"board"`).
/// * `verbs` - `(verb, op_string)` pairs for this noun.
/// * `op_args` - Per-op precomputed args keyed by op string.
/// * `fallback_args` - Args to use when an op has no per-op schema.
fn build_noun_command(
    noun: &str,
    verbs: &[(String, String)],
    op_args: &HashMap<String, (String, Vec<ArgMeta>)>,
    fallback_args: &[ArgMeta],
) -> Command {
    let mut cmd =
        Command::new(intern(noun.to_string())).about(intern(format!("{} operations", noun)));

    let mut verb_cmds: Vec<Command> = verbs
        .iter()
        .map(|(verb, op_str)| build_verb_command(verb, op_str, op_args, fallback_args))
        .collect();
    verb_cmds.sort_by(|a, b| a.get_name().cmp(b.get_name()));

    for verb_cmd in verb_cmds {
        cmd = cmd.subcommand(verb_cmd);
    }
    cmd
}

/// Build a single verb subcommand, scoping its args to that operation.
///
/// Uses the per-op args from `op_args` when present (keyed by `op_str`),
/// otherwise the `fallback_args`. The synthetic `op` parameter is skipped
/// because it is supplied by the noun-verb path, not a flag.
///
/// # Arguments
///
/// * `verb` - The verb name (e.g. `"init"`).
/// * `op_str` - The full op string (e.g. `"init board"`) used as the arg-map key.
/// * `op_args` - Per-op precomputed args keyed by op string.
/// * `fallback_args` - Args to use when this op has no per-op schema.
fn build_verb_command(
    verb: &str,
    op_str: &str,
    op_args: &HashMap<String, (String, Vec<ArgMeta>)>,
    fallback_args: &[ArgMeta],
) -> Command {
    // Use per-operation args if available, otherwise fall back to global
    let (about, args) = if let Some((desc, per_op_args)) = op_args.get(op_str) {
        (desc.as_str(), per_op_args.as_slice())
    } else {
        ("", fallback_args)
    };

    let about_str = if about.is_empty() {
        format!("{} operation", verb)
    } else {
        about.to_string()
    };

    let mut cmd = Command::new(intern(verb.to_string())).about(intern(about_str));

    for arg in args {
        if arg.name == OP_FIELD {
            continue;
        }
        cmd = cmd.arg(build_clap_arg(arg));
    }
    cmd
}

